package com.itwill.variable03;

public class VariableMain03 {

    public static void main(String[] args) {
        // 변수를 사용할 때 주의할 점.
        // 같은 이름으로 변수를 2번 이상 선언할 수 없다.

        int x = 1; //변수 선언
        // int x = 2; 같은 이름으로 변수 2번 이상 선언할 수 없다.
        System.out.println(x);
        
        x = 100; // 변수의 재할당
        System.out.println(x);
        
    }

}
