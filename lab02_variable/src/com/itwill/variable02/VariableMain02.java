package com.itwill.variable02;

public class VariableMain02 {

    public static void main(String[] args) {
        // 정수를 저장하는 변수(x)를 선언하고, 값을 저장(초기화)
        // 정수를 저장하는 변수(y)를 선언하고, 값을 저장(초기화)
        
        int x = 15;
        int y = 3;
        
        // 두 변수 X와 y의 사칙연산 값 출력
        System.out.println("x + y = " +(x+y));
        System.out.println("x - y = " +(x-y));
        System.out.println("x * y = " +(x*y));
        System.out.println("x / y = " +(x/y));
        System.out.println("x % y = " +(x%y));
        
        /*나누기 연산자
         * 정수/정수 결과는 나눈 "몫".
         * 실수/실수, 실수/정수 , 정수/실수 결과는 소수점까지 계산.
         */
        
    
    }

}
