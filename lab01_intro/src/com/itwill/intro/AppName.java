package com.itwill.intro; //패키지 선언문.

public class AppName {//클래스 선언
    
    public static void main(String[] args) {
        
        //주석(inline comment)
        
        /*(block comment) -> 문서화 주석시 사용
         * 클래스 설명, 메서드 설명
         * 메인함수는 있을 수도 없을 수도 있다.
         * 프로그램 전체에서 main 함수는 최소 1개 있어야한다.
         * 메인 method -> Java 프로그램 시작 */
        
        System.out.println("Hello Java");
        System.out.println("안녕");
        System.out.printf("%d\n", 33);
        
    }
    // main method 종료 -> Java program 종료
}
